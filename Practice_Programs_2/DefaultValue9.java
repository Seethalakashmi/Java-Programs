//find the default value for the given datatypes
class DefaultValue14
{
	static int a;
	static short b;
	static byte c;
	static long d;
	static float e;
	static double f;
	static char g;
	static boolean h;
	static String i;
	public static void main(String[] args) 
	{
		System.out.println("Default value of integer is: "+a);
		System.out.println("Default value of short is: "+b);
		System.out.println("Default value of byte is: "+c);
		System.out.println("Default value of long is: "+d);
		System.out.println("Default value of float is: "+e);
		System.out.println("Default value of double is: "+f);
		System.out.println("Default value of char is: "+g);
		System.out.println("Default value of boolean is: "+h);
		System.out.println("Default value of string is: "+i);
	}
}
